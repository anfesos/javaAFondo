package cap01;

import java.util.Scanner;

public class PrimerosNumeros1 {

	public static void main(String[] args) {
		Scanner scanner = new Scanner(System.in);
		
		int valor = scanner.nextInt();
		int i = 0;
		
		while(i<= valor){
			
			System.out.println("Iteración: " + i);
			i++;
			
		}

	}

}
