package co.com.aprendiendojava.javaafondo.cap01;

public class DemoCaracteres {

    public static void main(String[] args) {

        //Concatenando el objeto String "A"
        for(int i=0; i<5;i++){
            System.out.println(i+"A");
        }

        System.out.println();

        //Concatenando el caracter 'A'
        for(int i=0; i<5;i++){
            System.out.println(i+'A');
        }
    }
}
