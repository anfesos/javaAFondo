package co.com.aprendiendojava.javaafondo.cap01;

public class DemoCaracteresEspeciales {

    /*
    Algunos caracteres especiales son:
    \t - tabulador
    \n - salto de línea
    \" - comillas dobles
    \' - comillas simples
    \\ - barra
     */
    public static void main(String[] args) {

        System.out.println("Esto \t es un \"TABULADOR\"");
        System.out.println("Esto es un\nBARRA\nENE\n\n    :O");
        System.out.println("La barra es así: \\");
    }
}
